package ex_09_switch;

public class lab086_interview {
    static void main(String[] args) {
        char code ='C';
        switch (code){
            default:
                System.out.println("heloo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }
}
